public class MiniSongModel {
    private String tvSong;
    private String tvArtist;
    private String imageLabel;

    public String getTvSong() {
        return tvSong;
    }

    public void setTvSong(String tvSong) {
        this.tvSong = tvSong;
    }

    public String getTvArtist() {
        return tvArtist;
    }

    public void setTvArtist(String tvArtist) {
        this.tvArtist = tvArtist;
    }

    public String getImageLabel() {
        return imageLabel;
    }

    public void setImageLabel(String imageLabel) {
        this.imageLabel = imageLabel;
    }
}
